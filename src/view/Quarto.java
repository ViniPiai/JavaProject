    private void btnDormirChaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDormirChaoActionPerformed
        btnDormirChao.setEnabled(false);
        st.aumentarDisposicao(10);
        if(m.getSacoDormir()==0 || m.getCamaBox()==0 || m.getCamaMadeira()==0){
            st.t = new Timer(1000, new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    btnDormirChao.setEnabled(true);
                    st.t.stop();
                }
            });
            st.t.start();
        }
    }//GEN-LAST:event_btnDormirChaoActionPerformed

